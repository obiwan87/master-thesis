classifiers = {'MNB', 'NBSVM', 'RAE'};
best_parameters_results = deltas_results;

fig = figure;
fig.Color = 'w';
fig.Position = [ 776 254 910 710];
i = 1;
for k=1:4
    r = (k-1)*4 + (1:4);
    x = best_parameters_results.trainingSetSize(r);
    b = 5;
    
    % axis limits
    s = ((0:numel(classifiers)-1)*3 + 5);
    all_delta_means = table2array(best_parameters_results(r,s));
    y_max_lim = max(all_delta_means(:))*115;
    
    for li = 1:numel(classifiers)
        subplot(4,3,i+li-1)
        s = b + (li-1)*3;
        y = table2array(best_parameters_results(r,s))*100;        
        
        stem(1:4, y, 'filled', ...
            'MarkerSize', 4 ...
            );
        ax = gca;
        ax.XLim = [0.5 4.5];
        ax.YLim = [-2.5 y_max_lim];
        ax.XTickLabel = x;
        ax.XTick = 1:4;
        
        xlabel('T');
        ylabel('\Delta Acc. (%)');
        
        if k == 1
            title(classifiers{li});
        end
    end
    i = i + 3;
end

